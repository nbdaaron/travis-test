"""Application with math functions."""
def add_numbers(value_a, value_b):
    """Adds 2 numbers."""
    return value_a + value_b

def subtract_numbers(value_a, value_b):
    """Subtracts 2 numbers."""
    return value_a - value_b

def broken_add_numbers(value_a, value_b):
    """Adds 2 numbers but not really."""
    return value_a + value_b + 1
